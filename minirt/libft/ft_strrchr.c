#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*start;

	start = (char *)s;
	while (*s)
		s++;
	while (s != start && *s != ((unsigned char)c))
		s--;
	if (*s == (unsigned char)c)
		return ((char *)s);
	return (0);
}
